// ムを作りなさい。
// さらに、「0～30 までのいずれかの番号を入力すると、
// その番号のフィボナッチ数列を出力する」プログラムも作れるとなおよい。
//============================================================================//
int* fib(int *f, int n){
  f[0] = 1;
  f[1] = 1;
  for(int i = 2; i <= n; i++){
     f[i] = f[i - 1] + f[i - 2];
  }
  return f;
}
