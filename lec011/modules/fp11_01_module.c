//-----------------------------------------------------------------------//
// m. その他、自分が面白いと思う計算を行う関数を作って行なえ。
//-----------------------------------------------------------------------//
int max(int data[], int n){
  int result = data[0];
  for(int i = 1; i < n; i++){
    if(data[i] > result){
      result = data[i];
    }
  }
  return result;
}

int min(int data[], int n){
  int result = data[0];
  for(int i = 1; i < n; i++){
    if(data[i] < result){
      result = data[i];
    }
  }
  return result;
}

int calc1(int n, int t){
  int result = n;
  for(int i = 1; i < t; i++){
    result = result * (n - i);
  }
  return result;
}

//-----------------------------------------------------------------------//
// a. 円錐の底面の半径と高さを与え、体積を出力する。
//-----------------------------------------------------------------------//
double a(double r, double h) {
  double s = 3.14 * pow(r, 2.0) * h;
  double v = (1/3) * s * h;
  return v;
}


//-----------------------------------------------------------------------//
// b. 実数 x を与え、その平方値を出力する。
//-----------------------------------------------------------------------//
double b(double x){
  return sqrt(x);
}


//-----------------------------------------------------------------------//
// c. 実数 x を与え、その 8 乗 (または 7 乗または 6 乗) を出力する。
//-----------------------------------------------------------------------//
double c(double x, double n){
  return pow(x,n);
}


//-----------------------------------------------------------------------//
// d. 整数を 2 つ与え、その大きい方を出力する。
//-----------------------------------------------------------------------//
int d(int a, int b){
  int array[2] = {a, b};
  return max(array, 2);
}

//-----------------------------------------------------------------------//
// e. 整数を 2 つ与え、その小さい方を出力する。
//-----------------------------------------------------------------------//
int e(int a, int b){
  int array[2] = {a, b};
  return min(array, 2);
}


//-----------------------------------------------------------------------//
// f. 整数を 3 つ与え、その最大値を出力する (または 4 つの最大でもよい)。
