//-----------------------------------------------------------------------//
// h. 整数を 3 つ与え (すべて異なる値とする)、中央の値を出力する。
//-----------------------------------------------------------------------//
int h(int a,int b,int c){
  int array[3] = {a, b, c};
  int result = 0;
  int x = min(array,3);
  int i = min(array,3);
  for(int i = 0; i < 3; i++){
    if(array[i] != x && array[i] != x){
      result = array[i];
    }
  }
  return result;
}

//-----------------------------------------------------------------------//
// i. 正の整数 n を与え、n の階乗を出力する。
//-----------------------------------------------------------------------//
int i(int n){
  int result = n;
  for(int i = (n - 1); i > 0; i--){
    result = result * i;
  }
  return result;
}

//-----------------------------------------------------------------------//
// j. 正の整数 n を与え、2の乗 を出力する。
//-----------------------------------------------------------------------//
int j(int n){
  int result = 2;
  if (n == 0) {
    return 1;
  }
  for(int i = n; i > 1; i--){
    result = result * 2;
  }
  return result;
}

//-----------------------------------------------------------------------//
// k. 正の整数 n, r(n ≥ r) を与え、nCr を出力する。
//-----------------------------------------------------------------------//
int k(int n, int r){
  if(r > n){
    return FALSE;
  }
  return calc1(n, r)/calc1(r, r);
}

//-----------------------------------------------------------------------//
// l. 正の整数 a, b を与え、それらの最大公約数を出力する。
//-----------------------------------------------------------------------//
int l(int a, int b){
  int array[2] = {a, b};
  int m = min(array, 2);
  int result = 1;
  for(int i = 1; i <= m; i++){
    if(a % i == 0 && b % i == 0){
      result = i;
    }
  }
  return result;
}
